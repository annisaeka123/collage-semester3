    private void tblBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBukuMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblBuku.getModel();
        int row= tblBuku.getSelectedRow();
        Buku b =new Buku();
        
        b = b.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        txtIdBuku.setText (String.valueOf(b.getIdBuku()));
        cmbKategori.getModel().setSelectedItem(b.getKategori());
        txtJudul.setText(b.getJudul());
        txtPenerbit.setText(b.getPenerbit());
        txtPenulis.setText(b.getPenulis());
    }//GEN-LAST:event_tblBukuMouseClicked

